char	*ft_strupcase(char *str)
{
	int	i;
	int	n;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			n = 'a' - 'A';
			str[i] -= n;
		}
		i++;
	}
	return (str);
}
