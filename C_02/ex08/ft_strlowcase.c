char	*ft_strlowcase(char *str)
{
	int	i;
	int	k;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			k = 'a' - 'A';
			str[i] += k;
		}
		i++;
	}
	return (str);
}
